package com.rays.PreparedStatement;

public class Marksheetbean {
	
	private int id ;
	private String name ;
	private int rollNo ;
	private int phy ;
	private int chm ;
	private int maths ;
	
	public int getId() {
		return id ;
		
	}
	
	public void setId(int id ) {
		this.id=id;
	}
	
	public String getName() {
		return name ;
		
	}
	
	public void setName (String name) {
		this.name=name;
	}
	
	public int getRollNo() {
		return rollNo;
		
	}
	
	public void setRollNo(int rollNo) {
		this.rollNo=rollNo;        
		
	}
	
	public int getPhy() {
		return phy;
		
	}
	
	
	public void setPhy(int phy) {
		this.phy=phy;
		
	}
	
	public int getChm() {
		return chm ;
		
	}
	
	public void setChm(int chm ) {
		this.chm=chm;
		
	}
	
	public int getMaths() {
		return maths;
	}

	public void setMaths(int maths) {
		this.maths = maths;
	}

	
	}

	
	

